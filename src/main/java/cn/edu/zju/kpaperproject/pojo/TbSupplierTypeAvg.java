package cn.edu.zju.kpaperproject.pojo;

public class TbSupplierTypeAvg {
    private Integer id;

    private Integer experimentsNumber;

    private Integer cycleTimes;

    private Integer supplierType;

    private Integer avgActurePrice;

    private Integer avgActureQuality;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getExperimentsNumber() {
        return experimentsNumber;
    }

    public void setExperimentsNumber(Integer experimentsNumber) {
        this.experimentsNumber = experimentsNumber;
    }

    public Integer getCycleTimes() {
        return cycleTimes;
    }

    public void setCycleTimes(Integer cycleTimes) {
        this.cycleTimes = cycleTimes;
    }

    public Integer getSupplierType() {
        return supplierType;
    }

    public void setSupplierType(Integer supplierType) {
        this.supplierType = supplierType;
    }

    public Integer getAvgActurePrice() {
        return avgActurePrice;
    }

    public void setAvgActurePrice(Integer avgActurePrice) {
        this.avgActurePrice = avgActurePrice;
    }

    public Integer getAvgActureQuality() {
        return avgActureQuality;
    }

    public void setAvgActureQuality(Integer avgActureQuality) {
        this.avgActureQuality = avgActureQuality;
    }
}